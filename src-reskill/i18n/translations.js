export const translations = { en, de, cs };

export const DEFAULT_LOCALE = 'en';

/**
 * Detect the browser's preferred language and map it to a supported locale.
 * @returns {string} supported locale code (falls back to DEFAULT_LOCALE)
 */
export function detectBrowserLocale() {
  const browserLang = (typeof navigator !== 'undefined' && (navigator.language || navigator.userLanguage)) || '';
  if (!browserLang) return DEFAULT_LOCALE;
  const primary = browserLang.toLowerCase().split(/[-_]/)[0];
  return translations[primary] ? primary : DEFAULT_LOCALE;
}
